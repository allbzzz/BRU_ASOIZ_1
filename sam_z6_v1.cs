Задание 1
Для заданий 1 – 10 дана матрица целых чисел размером n×n.
Подсчитать среднее арифметическое четных элементов, расположенных ниже главной диагонали.
*/


using System;

class Program
{
    static void Main(string[] args)
    {
        int n = 5; // размер матрицы
        int[,] matrix = new int[n, n]; // объявление матрицы
        Random random = new Random(); // создание объекта класса Random для генерации случайных чисел

        // заполнение матрицы случайными числами
        for (int i = 0; i < n; i++)
        {
            for (int j = 0; j < n; j++)
            {
                matrix[i, j] = random.Next(10); // генерация случайного числа от 0 до 9
            }
        }

        // вывод матрицы на экран
        Console.WriteLine("Матрица:");
        for (int i = 0; i < n; i++)
        {
            for (int j = 0; j < n; j++)
            {
                Console.Write(matrix[i, j] + " ");
            }
            Console.WriteLine();
        }

        int count = 0; // количество четных элементов ниже главной диагонали
        int sum = 0; // сумма четных элементов ниже главной диагонали

        // подсчет среднего арифметического четных элементов ниже главной диагонали
        for (int i = 1; i < n; i++)
        {
            for (int j = 0; j < i; j++)
            {
                if (matrix[i, j] % 2 == 0) // если элемент четный
                {
                    count++;
                    sum += matrix[i, j];
                }
            }
        }

        double average = (double)sum / count; // среднее арифметическое четных элементов ниже главной диагонали

        Console.WriteLine("Среднее арифметическое четных элементов ниже главной диагонали: " + average);
    }
}