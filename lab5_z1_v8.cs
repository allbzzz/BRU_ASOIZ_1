/*
Лабораторная 5, задание 1, вариант 8
Написать алгоритм, который по номеру дня недели - целому числу от 1 до 7 
выдавать в качестве результата количество пар в Вашей группе в 
соответствующий день.
*/


using System;

class Program
{
    static void Main()
    {
        Console.Write("Введите номер дня недели (1-7, где 1 - понедельник, 7 - воскресенье): ");
        int dayNumber = Convert.ToInt32(Console.ReadLine());

        int classesCount = dayNumber switch
        {
            1 => 5,  // Понедельник
            2 => 4,  // Вторник
            3 => 6,  // Среда
            4 => 5,  // Четверг
            5 => 3,  // Пятница
            6 => 0,  // Суббота
            7 => 0,  // Воскресенье
            _ => -1  // Некорректное значение
        };

        if (classesCount == -1)
        {
            Console.WriteLine("Некорректное значение. Пожалуйста, введите число от 1 до 7.");
        }
        else
        {
            Console.WriteLine($"Количество пар в день {dayNumber}: {classesCount}");
        }
    }
}